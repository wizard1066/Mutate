import SwiftUI

struct Fonts {
  static func touchOfNature (size: CGFloat) -> Font {
    return Font.custom("TouchOfNature", size: size)
  }
  
  static func avenirNextCondensedBold (size: CGFloat) -> Font {
    return Font.custom("AvenirNextCondensed-Bold", size: size)
  }
  
  static func avenirNextCondensedMedium (size: CGFloat) -> Font {
    return Font.custom("AvenirNextCondensed-Medium", size: size)
  }
  
  static func futuraCondensedExtraBold (size: CGFloat) -> Font {
    return Font.custom("Futura-CondensedExtraBold", size: size)
  }
  
  static func futuraCondensedMedium (size: CGFloat) -> Font {
    return Font.custom("Futura-CondensedMedium", size: size)
  }
  
  

  
  static func zapfino (size: CGFloat) -> Font {
    return Font.custom("Zapfino", size: size)
  }
}
